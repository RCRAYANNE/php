<p>  A 22ª edição do campeonato mundial de futebol está perto de acontecer. Pela primeira vez na
     história do futebol, o Catar sediará uma Copa do Mundo e, pela primeira vez, devido às altas temperaturas, 
     as partidas não serão disputadas no verão, mas no inverno! De 21 de novembro a 18 de dezembro, as melhores equipes
     do mundo lutarão pelo título da Copa do Mundo no período de Natal. A 22ª edição do torneio deve ser o último no formato atual, com 32
     times. A FIFA já anunciou que, a partir de 2026, 48 seleções estarão no maior torneio da Terra.</p>
<p>Uma outra particularidade dessa Copa é a dimensão territorial do país-sede. O Catar ocupa uma área de apenas 11,6 mil km², 
     bem menor que dos últimos países que receberam o evento: Rússia (17,1 milhões km²) e Brasil (8,5 milhões de km²). Com isso, as seleções
      não precisarão viajar de avião de uma cidade para outra e todos os trajetos serão realizados de ônibus.</p>
      <p>O Catar possui 2,7 milhões de habitantes e espera receber mais de 1 milhão de turistas durante a Copa do Mundo. Os
     ingressos só serão vendidos em 2022, depois do sorteio dos grupos.</p>

     <h4>Escolha da sede e polêmicas</h4>
     <p> O processo de escolha para as sedes das Copas de 2018 e 2022 teve início em 2009, e 11 propostas foram 
         recebidas pela federação. Posteriormente, houve a desistência do México e a candidatura da Indonésia foi rejeitada pela FIFA.</p>
     <p>Durante o processo de escolha do país-sede da Copa de 2022, restaram cinco propostas: Austrália, Catar, 
         Coreia do Sul, Estados Unidos e Japão. Em novembro de 2010, houve uma reunião dos membros do Comitê Executivo da FIFA, em Zurique, 
         para escolher as sedes das Copas, na qual o Catar recebeu o maior número de votos e foi escolhido para sediar o campeonato de 2022,
        assim como a Rússia, que sediou o mundial em 2018.</p>
     <p>A vitória do Catar foi altamente criticada após os inúmeros escândalos de corrupção envolvendo a FIFA, 
         incluindo a denúncia de que o governo do país havia pagado à instituição para poder receber o torneio. As denúncias acusaram muitos 
         ex-dirigentes e colaboradores da FIFA de pagamento e recebimento de propina, entre outros delitos.</p>
     <p> Em março de 2019, o jornal britânico The Sunday Times informou que o Catar teria comprado os votos em favor
          de sua candidatura e pagado 880 milhões de euros à FIFA, 21 dias antes da definição do país-sede. O pagamento teria sido realizado em 
          duas parcelas, de 400 milhões e de 480 milhões de euros, além de uma bonificação de 100 milhões se o Catar fosse escolhido. O valor 
          seria repassado pela rede de televisão Al Jazeera, controlada pelo governo.   
     <p> As críticas, polêmicas e denúncias contra o Catar não pararam em problemas climáticos e propinas.
               O comitê organizador da Copa do Catar também foi questionado pelas condições de trabalho dos operários, na construção dos 
               estádios, denunciadas pela ONG Anistia Internacional.
     <p> Nos últimos anos, a Anistia Internacional já vinha fazendo alguns comunicados a respeito das condições 
         precárias dos trabalhadores imigrantes nas obras no Qatar. De acordo com um relatório divulgado em novembro do ano passado, a ONG
          mostra que não houve progresso no mercado de trabalho no Qatar em 2021. Além disso, práticas abusivas ressurgiram, o que deixou a 
          situação dos operários ainda mais difícil.
     <p>Foi marcado um encontro no qual a Anistia Internacional deve entregar uma nova petição à Fifa, que 
         concordou que vai conversar para ajustar os pontos a serem melhorados.
     </p>
    

    
