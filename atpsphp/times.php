<h3>Quais são as seleções classificadas para a Copa do Mundo do Qatar 2022?</h3></h3>
<div id="conteudo"> <!--inicio da div-->


<img class="imgs" src="imagem/imagem_times.jpg" title="copa do mundo" width="700"/>



<p>Várias seleções já vão garantindo suas vagas para disputar o torneio.
    O Brasil, por exemplo, já está garantido na Copa e vai em busca do hexa.</p>
    <p>Compilamos, então, todas as seleções que já confirmaram suas vagas na Copa do Mundo de 2022.</p>

<p><strong>Confira!</strong></p>

 <h3>QATAR (país sede)</h3>

<p>O Qatar, país-sede da Copa do Mundo, já está confirmado.A seleção árabe vai
   para o seu primeiro Mundial em 2022, sem precisar disputar as eliminatórias,
    já que irá sediar o torneio (e sabe que irá jogar no Grupo A).</p>

  <h3>Brasil (Conmebol)</h3>

<p>O Brasil garantiu a sua vaga na Copa do Mundo de 2022 em 11 de novembro de
     2021, mais de um ano antes do início do Mundial.</p>
<p>Com uma campanha muito boa nas Eliminatórias Sul-Americanas, a equipe que desagradou
    em alguns momentos, mas que se saiu muito bem na América do Sul com uma defesa
     aparentemente intransponível, um sistema de jogo sólido e Neymar chamando a 
     responsabilidade no ataque.Tudo indica, então, que o Brasil irá para a Copa
      como um dos cabeças-de-chave da competição.
  </p>


  <h3>Argentina (CONMEBOL)</h3>

<p>A Argentina se tornou a segunda seleção sul-americana a se classificar para a 
    Copa do Mundo de 2022.

<p>Vivendo grande fase sob o comando de Lionel Scaloni, a atual campeã da Copa América chega embalada
para tentar dar a Messi o único título que ainda lhe falta na carreira.</p>

<p>Parecendo finalmente ter encontrado um sistema defensivo, a seleção argentina 
chega muito forte em busca do tricampeonato.</p>

  <h3>Espanha (UEFA)</h3>

<p> A Espanha não teve dificuldades em seu grupo nas Eliminatórias Europeias 
  (grupo que também incluía Suécia, Grécia, Geórgia e Kosovo), passou em 
  primeiro e chega empolgada para a Copa do Mundo.</p>

  <p>Trata-se, afinal, de um time jovem e que parece evoluir cada dia mais, 
    com vários jogadores com idade olímpica, claro que conta também os com veteranos. </p>

    <h3>Sérvia (UEFA)</h3>

  <p>Uma das grandes surpresas entre as seleções classificadas para a Copa do Mundo, 
     ao menos na primeira fase das Eliminatórias Europeias</p>
  <p>A equipe da Sérvia ganhou de 2 a 1 da equipe de Portugal na última rodada da fase 
    de grupos das eliminatórias, com gols de Tadic e Mitrovic, mandando a equipe perdedora para a repescagem.</p>
  <p>Vale lembrar que, em 2018, a Sérvia ficou no grupo do Brasil na primeira fase da
     Copa da Rússia, mas terminou eliminada com apenas três pontos, atrás do próprio 
     Brasil e da Suíça.</p>

   <h3>França (UEFA)</h3>

  <p> França, atual campeã da Copa do Mundo, tem para muitos a seleção mais poderosa do mundo, com nomes 
      de destaque para todas as posições e um elenco que dá ao treinador Didier Deschamps muitas opções.</p>
  <p>A equipe, porém, não conseguiu convencer nas Eliminatórias, tendo sofrido contra seleções como Bósnia,
    Finlândia e Ucrânia e terminado com três empates em oito jogos.</p>
  <p>De qualquer jeito a França chega como uma das favoritas a conquistar o título no Qatar. </p>

     <h3>Suíça (UEFA)</h3>

  <p>Indo para a sua quinta Copa do Mundo consecutiva, a Suíça segue sendo uma das seleções mais consistentes do continente europeu.</p>

  <p>Chega empolgada, inclusive, por ter mandado a Itália para a repescagem no Grupo C das Eliminatórias Europeias. Não deve ser 
    cabeça de chave, porém, devido à sua posição no ranking da Fifa - e pode novamente ser adversária do Brasil na fase de grupos.</p>

       <h3>Bélgica (UEFA)</h3>

   <p>Algoz do Brasil em 2018 e líder do ranking da Fifa, a Bélgica chega com tudo para a Copa do Mundo de 2022 naquela que pode ser 
               o último Mundial de sua geração mais gloriosa.</p>
   <p>Com seis vitórias e dois empates em oito jogos nas Eliminatórias, Lukaku, De Bruyne, Courtois, Tielemans e cia se 
                 classificaram com relativa facilidade e chegarão no Qatar como cabeças de chave.</p>

      <h3>Dinamarca (UEFA)</h3>

    <p>A dinamarca se tornou a segunda classificada para a copa do mundo, com 27 pontos de 30 e garantiu sua vaga 
      ainda em outubro.</p>
    <p>Com um time repleto de bons jogadores, a Dinamarca chega para um mundial onde pode surpreender.</p>

      <h3>Holanda (UEFA)</h3>

    <p>Após ficar de fora da Copa do Mundo em 2018, a Holanda garante sua vaga para a Copa do Mundo de 2022 no Qatar. 
      Os atacantes Steven Bergwijn e Memphis Depay marcaram no final do jogo e garantiram a classificação com uma
     vitória por 2 a 0 sobre a Noruega, consolidando a liderança do Grupo G das Eliminatórias europeias.
     </p>

     <h3>Croácia (UEFA)</h3>

     <p>Jogando em casa, a Croácia venceu a Rússia por 1 a 0, em partida válida pela última rodada do Grupo H das Eliminatórias
       Europeias para a Copa do Mundo de 2022. Kudryashov marcou contra, aos 31 do segundo tempo, e definiu o triunfo dos donos
        da casa. </p>
      <p>Vice-campeã da última Copa do Mundo, após campanha histórica vários dos mesmos nomes que brilharam em 2018 querem fazer
         história novamente no Qatar.</p>

      <h3> Inglaterra (UEFA) </h3>

      <p>Com a vaga bem encaminhada desde a última rodada, mas ainda sem a confirmação, a Inglaterra carimbou o passaporte para o 
        Oriente Médio e disputará a Copa do Mundo de 2022. Na rodada final das Eliminatórias Europeias, a 
        Inglaterra bateu o time de San Marino por 10 a 0. </p>
        
       <h3>Alemanha (UEFA)</h3>

       <p>A Alemanha se classificou para a Copa do Mundo de 2022 com uma goleada sobre a Macedônia do Norte por 4 x 0 fora de casa,
          garantindo a primeira colocação no Grupo J das eliminatórias com duas rodadas de antecipação.  </p>
        <p>Primeira seleção a se classificar para o Mundial, a seleção alemã se aproveitou de um 
          grupo mais fraco, fez 27 pontos em 30 disputados e pôde se dar ao luxo de realizar alguns testes na reta final.</p>

        <h3>Irã (Ásia)</h3>

        <p>A seleção do Irã é a primeira da Ásia a conquistar vaga na Copa do Mundo de 2022. A classificação foi sacramentada,
           com a vitória por 1 a 0 sobre o Iraque, em partida disputada no estádio Azadi, em Teerã. O Catar também representa 
          o continente asiático, mas já estava garantido por ser país-sede.</p>

        <h3>Coreia do Sul (Ásia)</h3>

        <p>Com uma vitória por 2 a 0 contra a Síria, nesta terça-feira, 1, a Coreia do Sul se classificou para a sua 11ª Copa do Mundo.
           O jogo foi realizado nos Emirados Árabes, já que a Síria não pode jogar em casa por causa da guerra civil no país. Apesar de 
           a seleção sul-coreana ter entrado em campo sem seu craque Son Heung-Min, do Tottenham, que está contundido, a equipe dominou
            o jogo e marcou com Kim Jin-Su e Kwon Chang-Hoon. </p>

        <h3>Japão (Ásia)</h3>

         <p>O Japão se classificou nesta quinta-feira para a Copa do Mundo do Catar-2022 ao derrotar a Austrália por 2-0 em Sydney.</p>
         <p>Dois gols de Kaoru Mitoma no fim da partida (89 e 90+4) decretaram a vitória dos japoneses, que garantiram uma das duas vagas
         do Grupo B das eliminatórias da Ásia.</p>
         <p>O Japão disputará a sétima Copa do Mundo consecutiva. Na Rússia-2018, a seleção nipônica foi eliminada pela Bélgica (3-2) 
           nas oitavas de final.
        </p>

        <h3>Arábia Saudita (Ásia)</h3>

        <p>Como consequência da vitória do Japão sobre a Austrália, a Arábia Saudita pôde comemorar sua classificação mesmo tendo empatado
           em 1 a 1 com a China. Os sauditas, que disputavam com os australianos uma vaga, não podem mais serem alcançados na zona de 
           classificação do Grupo B.</p>

                 

