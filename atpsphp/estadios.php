<h3>Conheço os 8 estádios que sediarão os jogos da Copa do Mundo 2022, no Catar</h3></h3>
<div id="conteudo"> <!--inicio da div-->



<img class="imgs" src="imagem/est_copa.jpg" title="copa do mundo" width="700"/>

<p>Serão oito estádios espalhados pelas sete cidades escolhidas como sedes da competição. 
Conhecido como um dos países mais ricos do mundo, o Qatar promete realizar um dos Mundiais com as arenas mais luxuosas da história. 
Entre eles, apenas o Khalifa foi reformado e seguirá com sua capacidade original de 40 mil lugares depois da competição. Os outros 
sete estádios foram construídos ou estão em fase de construção. Entre eles, seis terão suas capacidades reduzidas após o mundial e um 
será desmontado. Depois da Copa, o Catar vai doar 170 mil assentos para estádios de outros países.</p>

<p>A proximidade dos estádios da Copa de 2022 permitirá aos torcedores acompanhar mais de um jogo por dia. A maior distância será de 55 km 
entre os estádios de Al Khor, que fica ao norte, e Al Wakrah, no sul.</p>
<p>Com projetos ambiciosos, mesmo com a mudança nas datas para a prevenção do calor, todos os oito estádios terão um sistema de climatização para 
amenizar a temperatura.</p>

<p><strong>Conheça os estádios da Copa do Catar:</strong></p>


<h3>Estádio Khalifa (Doha)</h3>
<img class="imgs" src="imagem/est_khalifa09.jpg" title="Estadio Khalifa" width="700"/>
<p>O estádio Khalifa foi reformado e ficou pronto em maio de 2017. Ele foi construído 
    em 1976 e é o mais tradicional do país. Está localizado em Doha e tem capacidade para
     40 mil torcedores e será equipado com "tecnologia de resfriamento inovadora" que permitirá que os jogadores atuem em um ambiente confortável.
      A estrutura teve a adição de um teto único para abrigar as arquibancadas, ao lado de uma nova estrutura na ala leste que terá praças de alimentação,
       lojas, salas para vários propósitos, áreas VIPs, museu, e um centro médico. Os jogos serão realizados nesse local até as quartas de final.</p>

<h3>Estádio Al Janoub (Al Wakrah)</h3>
<img class="imgs" src="imagem/est_al.jpg" title="Al Janoub" width="700"/>
<p>Na cidade de Al Wakrah, o estádio Al Janoub foi projetado pela arquiteta Zaha Hadid em parceria com AECOM. Com uma forma bem diferente do convencional,
   suas linhas graciosas e curvas fluidas foram inspiradas nos tradicionais barcos dhow que tecem as correntes do Golfo. Por dentro, vigas curvadas sustentam
    o teto, lembrando o casco de um navio e permitindo que a luz inunde a arena.</p>
    <p>O recinto de 40 mil lugares foi inaugurado em 16 de maio de 2019, coincidindo com a final da Copa Amir. Após a Copa do Mundo, a capacidade do estádio será 
      reduzida para 20 mil torcedores, e os outros 20 mil assentos serão doados a projetos de desenvolvimento de futebol em outros países. Unidades de recreação e
       hospitalidade, assim como uma escola, espaço de eventos, pistas de ciclismo, hipismo e corrida são algumas das novas amenidades que estão sendo desenvolvidas
        juntamente do estádio para garantir seu uso no longo prazo.</p>

<h3>Estádio Ahmad Bin Ali</h3>
<img class="imgs" src="imagem/est_ahmad.jpg" title="Estadio Ahmad Bin Ali" width="700"/>
<p>Casa de um dos times mais tradicionais do país, o Al Rayyan Sports Club, o estádio Ahmad bin Ali celebra a união de sua comunidade ao incorporar símbolos das 
  tradições e da cultura catari. Uma das principais características do projeto é a fachada ondulada, composta por padrões que caracterizam diferentes aspectos do país, 
  como "a importância da família, a beleza do deserto, a flora e a fauna nativas, além do comércio local e internacional". 40 mil torcedores ficarão protegidos por uma 
  cobertura leve e por sistemas de resfriamento avançados, garantindo conforto durante as partidas. Batizada de "portal do deserto", a arena abriu suas portas em dezembro
   de 2020, para final da Copa Amir.</p>
   <p>As instalações ao redor do Ahmad Bin Ali também refletem o país, com estruturas em forma de dunas de areia. O projeto foi realizado por BDP Pattern, Ramboll e AECOM.</p>

<h3>Estádio 974</h3>
<img class="imgs" src="imagem/est_974.jpg" title="Estádio 974" width="700"/>
<p>Considerado um dos designs mais inovadores dentre todas as 8 estruturas, o Estádio 974, conhecido anteriormente como Ras Abou Aboud, é inspirado pelo comércio 
  internacional e marítimo do Catar. O número 974 é altamente significativo para o projeto, já que é o código internacional telefônico do país, assim como o número de
   containers utilizados na construção. A estrutura fica na orla oposta ao skyline da Baía Oeste de Doha. Já que é majoritariamente feito por containers de navios, 
   o estádio de 40 mil lugares será completamente desmontado após o fim da Copa do Mundo e utilizado como um complexo à beira mar para a comunidade local. </p>
  <p> O estádio foi inaugurado no dia 20 de novembro de 2021, em um lançamento digital.</p>

  <h3>Estádio Al Thumama</h3>
  <img class="imgs" src="imagem/est_thumama.jpg" title="Al Thumama" width="700"/>
  <p>Localizado 12 quilômetros ao sul de Doha, o estádio Al Thumama entrelaça a cultura árabe por sua fachada,O formato circular do estádio Al Thumama homenageia o gahfiya,
      espécie de boné tradicionalmente utilizado por homens e meninos no Oriente Médio durante séculos. </p>
  <p>Além de seu desenho, a área em volta do projeto explora a importância da arquitetura ecológica e contextual, buscando a certificação de quatro estrelas do 
    Sistema de Avaliação de Sustentabilidade (GSAS) tanto para o design quanto para a construção. O estádio garante a economia de 40% a mais de água do que um estádio 
    convencional, utilizando água reciclada para irrigar áreas verdes. 50.000 km² de área de parque são integrados no projeto, que utiliza árvores nativas para cobrir 84% 
    da paisagem. O estádio foi inaugurado na 49ª Final da Copa Amir, em 22 de outubro de 2021.</p>

  <h3>Estádio Education City</h3>
  <img class="imgs" src="imagem/est_edu.jpg" title="Estádio Education City" width="700"/>
  <p>Com capacidade para 40 mil pessoas, o estádio Education City está localizado entre algumas das principais universidades do Catar. Sua fachada apresenta padrões triangulares,
     lembrando diamantes que parecem mudar de cor ao longo do dia de acordo com o movimento do sol no céu.</p>
     <p> Devido à sua localização particular na Cidade da Educação, a estrutura foi desenhada com grande foco em acessibilidade. Durante a construção, escavadores descobriram 
       rochas que datam de 20 a 30 milhões de anos atrás, o que os forçou a escavar mais 17 metros para baixo, para que o campo fique ao nível do mar, onde as temperaturas 
       são mais frescas. </p>
  <p>Fenwick Iribarren Architects foram os responsáveis pelo projeto que teve o design foi revelado em 2014, e o estádio foi finalizado e aberto ao público em 2020. </p>

  <h3>Estádio Al Bayt</h3>
  <img class="imgs" src="imagem/est_bayt.jpg" title="Estádio Al Bayt" width="700"/>
  <p>Inspirado pelas barracas tradicionais nômades, o estádio de 60 mil lugares deve ser o palco da partida de estreia da Copa do Mundo FIFA 2022, e vai receber partidas 
    até a fase semifinal. Localizado na cidade de Al Khor, no norte, cidade famosa pelos mergulhadores e pesca de pérolas que sempre convidaram os habitantes do deserto até
     a costa ao longo dos anos, o design do estádio homenageia o passado e o presente do Catar, destacando a cultura, tradições e história da cidade. Semelhante às barracas 
     nômades, a parte superior da arquibancada é um design modular portátil, que será removido no decorrer do campeonato, e oferecido a países em desenvolvimento necessitados
      de infraestrutura para a prática de esportes. O projeto do estádio foi revelado em meados de 2014 pelo Comitê Supremo para Entrega e Legado (SC) e Fundação Aspire Zone,
       e foi inaugurado no início de 2020 no dia nacional dos Esportes no Catar.</p>

  <h3>Estádio Lusail</h3>
  <img class="imgs" src="imagem/est_lusail.jpg" title="Estádio Lusail" width="700"/>
   <p>Com capacidade para 80 mil pessoas, o Estádio Lusail é o maior dos 8 recintos, e vai receber a final da Copa do Mundo FIFA Catar 2022. O design é inspirado pelo jogo 
     de luz e sombra visto nas lanternas Fanar. A forma estrutural e a fachada repetem os motivos intricados de peças de arte e mobiliário encontradas pelos mundos árabe e
      islâmico desde suas primeiras civilizações. Devido à sua localização proeminente em Lusail, uma nova metrópole construída, o planejamento master do estádio apresenta 
      um sistema de bondes, e áreas verdes exuberante, o que o transforma em uma arena focada em sustentabilidade que se volta para as necessidades humanas e para a preservação
       ambiental.</p>
    <p> Com o fim do evento, o local será transformado em um hub comunitário polivalente, com escolas, lojas, cafés, instalações esportivas e clínicas de saúde.
        O estádio foi inaugurado no final de 2021. O projeto é de Foster + Partners e Populous.</p>

